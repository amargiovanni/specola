# ---------------------------------------------------------------------------
# Labels
# ---------------------------------------------------------------------------
_LABELS = {
    "it": {
        "archive": "Archivio",
        "number": lambda n: "Specola" if n == 1 else "Specole",
        "today": "Oggi",
        "footer": "Generato da Specola",
    },
    "en": {
        "archive": "Archive",
        "number": lambda n: "briefing" if n == 1 else "briefings",
        "today": "Today",
        "footer": "Generated by Specola",
    },
}


# ---------------------------------------------------------------------------
# Private helpers
# ---------------------------------------------------------------------------

def _strip_inline_markdown(text: str) -> str:
    """Remove bold markers and links, keeping plain text."""
    text = _LINK_RE.sub(r"\1", text)
    text = _BOLD_RE.sub(r"\1", text)
    return text.strip()


def _date_from_filename(name: str) -> str | None:
    """Extract ISO date string from a filename like 'Specola_2026-04-05.html'."""
    m = _DATE_IN_FILENAME_RE.search(name)
    return m.group(1) if m else None


def _extract_preview_from_html(html_path: Path, max_items: int = 3) -> list[str]:
    """Return up to max_items plain-text list items from the HTML file."""
    try:
        content = html_path.read_text(encoding="utf-8")
    except OSError:
        return []

    items = re.findall(r"<li>(.*?)</li>", content, re.DOTALL)
    result = []
    for raw in items[:max_items]:
        plain = _HTML_TAG_RE.sub("", raw)
        plain = plain.strip()
        if plain:
            result.append(plain)
    return result


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def extract_highlights(markdown: str) -> list[str]:
    """Extract up to 5 plain-text highlights from 'Da sapere oggi' / 'Key takeaways'.

    Returns an empty list if the section is not found.
    """
    lines = markdown.split("\n")
    in_section = False
    results: list[str] = []

    for line in lines:
        stripped = line.strip()

        if stripped in _HIGHLIGHT_HEADINGS:
            in_section = True
            continue

        if in_section:
            # Stop at the next H2/H1 heading
            if stripped.startswith("## ") or stripped.startswith("# "):
                break

            # Bullet: dash or star
            if stripped.startswith("- ") or stripped.startswith("* "):
                text = _strip_inline_markdown(stripped[2:])
                if text:
                    results.append(text)

            # Numbered item
            elif _NUMBERED_RE.match(stripped):
                text = _strip_inline_markdown(_NUMBERED_RE.sub("", stripped))
                if text:
                    results.append(text)

            if len(results) >= 5:
                break

    return results


def regenerate_portal_index(output_dir: str | Path, language: str = "it") -> str:
    """Scan output_dir for Specola_*.html files and regenerate index.html.

    Returns the path to the generated index.html.
    """
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    labels = _LABELS.get(language, _LABELS["it"])
    today_str = date.today().isoformat()

    # Collect briefing files (exclude index.html itself)
    entries: list[tuple[str, Path]] = []
    for html_file in output_dir.glob("Specola_*.html"):
        if html_file.name == "index.html":
            continue
        date_str = _date_from_filename(html_file.name)
        if date_str:
            entries.append((date_str, html_file))

    # Sort newest first
    entries.sort(key=lambda x: x[0], reverse=True)

    # Build cards
    card_parts: list[str] = []
    for date_str, html_file in entries:
        is_today = date_str == today_str
        card_class = 'class="card today"' if is_today else 'class="card"'
        label_html = (
            f'<span class="card-label">{labels["today"]}</span>' if is_today else ""
        )

        preview_items = _extract_preview_from_html(html_file, max_items=3)
        if preview_items:
            li_items = "\n".join(f"      <li>{item}</li>" for item in preview_items)
            preview_html = f"    <ul>\n{li_items}\n    </ul>"
        else:
            preview_html = ""

        card_parts.append(
            f'<a href="{html_file.name}" {card_class}>\n'
            f'  <div class="card-header">\n'
            f'    <span class="card-date">{date_str}</span>\n'
            f"    {label_html}\n"
            f"  </div>\n"
            f"{preview_html}\n"
            f"</a>"
        )

    cards_html = "\n".join(card_parts)
    count = len(entries)
    number_label = labels["number"](count)

    html = _INDEX_TEMPLATE.format(
        lang=language,
        archive_label=labels["archive"],
        count=count,
        number_label=number_label,
        cards=cards_html,
        footer_label=labels["footer"],
        today_display=today_str,
    )

    index_path = output_dir / "index.html"
    index_path.write_text(html, encoding="utf-8")
    return str(index_path)
