"""Portal index generator and highlight extraction for Specola."""
from __future__ import annotations

import re
from datetime import date
from pathlib import Path

# ---------------------------------------------------------------------------
# Regex helpers
# ---------------------------------------------------------------------------
_BOLD_RE = re.compile(r"\*\*(.+?)\*\*")
_LINK_RE = re.compile(r"\[([^\]]+)\]\([^)]+\)")
_NUMBERED_RE = re.compile(r"^\d+\.\s+")
_HTML_TAG_RE = re.compile(r"<[^>]+>")
_DATE_IN_FILENAME_RE = re.compile(r"Specola_(\d{4}-\d{2}-\d{2})")

# ---------------------------------------------------------------------------
# Sections that contain the highlights (IT and EN)
# ---------------------------------------------------------------------------
